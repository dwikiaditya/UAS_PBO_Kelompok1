    private void btnTglActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTglActionPerformed
        // TODO add your handling code here:
        //Membuat object calendar dari class Calendar dengan isi method getInstance pada class Calendar
        Calendar calendar = Calendar.getInstance();
        //Membuat format tanggal baru = Hari-Bulan-Tahun
        SimpleDateFormat format = new SimpleDateFormat("dd-MM-yyyy");
        //Merubah format tanggal pada method getTime() menjadi Hari-Bulan-Tahun
        String date = format.format(calendar.getTime());
        //Mengeset nilai txtPinjam dengan variabel date
        txtTanggal.setText(date);
        //Menambah 7 hari dari hari ini
        calendar.add(Calendar.DAY_OF_MONTH, 7);
        //Merubah format tanggal pada method getTime() menjadi Hari-Bulan-Tahun
        
    }//GEN-LAST:event_btnTglActionPerformed

