    private void comboPilihActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comboPilihActionPerformed
        // TODO add your handling code here:
        pilihan=(String)comboPilih.getSelectedItem();
        //pilihan kode dibuat dengan switch case 
        switch (pilihan){
            case "01":
                txtNamaBrg.setText("Laptop");
                harga_brg=10000000;
            break;
                //pada case 1, case yang dipilih, variabel txtNamaBrg akan diisi value dengan masing masing value di casenya
                //dengan nama barang laptop pada pilihan pertama
                //dengan harga barang 10.000.000
                //jika kondisi sudah terpenuhi maka coding berhenti
            case "02":
                txtNamaBrg.setText("Mouse");
                harga_brg=100000;
            break;
            //pada case 2, case yang dipilih, variabel txtNamaBrg akan diisi value dengan masing masing value di casenya
                //dengan nama barang Mouse pada pilihan kedua
                //dengan harga barang 100.000
                //jika kondisi sudah terpenuhi maka coding berhenti
            case "03":
                txtNamaBrg.setText("Keyboard");
                harga_brg=200000;
            break;
            //pada case 3, case yang dipilih, variabel txtNamaBrg akan diisi value dengan masing masing value di casenya
                //dengan nama barang Keyboard pada pilihan ketiga
                //dengan harga barang 200.000
                //jika kondisi sudah terpenuhi maka coding berhenti
            case "04":
                txtNamaBrg.setText("Charger");
                harga_brg=100000;
            break;
            //pada case 4, case yang dipilih, variabel txtNamaBrg akan diisi value dengan masing masing value di casenya
                //dengan nama barang Charger pada pilihan keempat
                //dengan harga barang 100.000
                //jika kondisi sudah terpenuhi maka coding berhenti
            case "05":
                txtNamaBrg.setText("Flashdisk");
                harga_brg=45000;
            break;
            //pada case 5, case yang dipilih, variabel txtNamaBrg akan diisi value dengan masing masing value di casenya
                //dengan nama barang Flashdisk pada pilihan kelima
                //dengan harga barang 45.000
                //jika kondisi sudah terpenuhi maka coding berhenti
            case "06":
                txtNamaBrg.setText("Speaker");
                harga_brg=500000;
            break;
            //pada case 6, case yang dipilih, variabel txtNamaBrg akan diisi value dengan masing masing value di casenya
                //dengan nama barang speaker pada pilihan keenam
                //dengan harga barang 500.000
                //jika kondisi sudah terpenuhi maka coding berhenti
            case "07":
                txtNamaBrg.setText("Monitor");
                harga_brg=1000000;
            break;
            //pada case 3, case yang dipilih, variabel txtNamaBrg akan diisi value dengan masing masing value di casenya
                //dengan nama barang Monitor pada pilihan ketujuh
                //dengan harga barang 1.000.000
                //jika kondisi sudah terpenuhi maka coding berhenti
        }
        txthargaBarang.setText(""+Integer.toString(harga_brg));
        //pada variabel txthargaBarang, valuenya akan terisi sesuai saat salah satu case dipilih
    }//GEN-LAST:event_comboPilihActionPerformed
